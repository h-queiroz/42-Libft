#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int		i;
	const unsigned char	*my_s1;
	const unsigned char	*my_s2;

	my_s1 = s1;
	my_s2 = s2;
	i = 0;
	while (i < n)
	{
		if (my_s1[i] != my_s2[i])
			return (my_s1[i] - my_s2[i]);
		i++;
	}
	return (0);
}
